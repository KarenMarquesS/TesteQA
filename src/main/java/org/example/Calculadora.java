package org.example;

public class Calculadora {

    public int soma(int a, int b){
        return a + b;
    }

    public int subtrair(int a, int b){
        return a - b;
    }

    public int multiplicacao(int a, int b){
        return a * b;
    }

    public int divisao(int a, int b){
        return a / b;
    }
}
